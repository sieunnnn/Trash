package multiCampus.operator;

public class 산술연산자 {
    public static void main(String[] args) {
        int x = 10;
        int y = 3;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x / y); // 몫
        System.out.println(x % y); // 나머지
        System.out.println(10.0 / y);
        System.out.println(x * y);
    }
}
